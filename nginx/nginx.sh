#!/bin/zsh

EXPOSE=8800
NGINX_NAME=nginx_test

PACKAGE=/Users/sun/go/SourceCode/src/github.com/CharlesBases/common/nginx/
CONF=$PACKAGE/conf/nginx.conf
HTML=$PACKAGE/html
LOGS=$PACKAGE/logs
SERV=$PACKAGE/conf/conf.d

docker stop $NGINX_NAME
docker rm -f $NGINX_NAME

docker run \
-d \
-p $EXPOSE:8080 \
-v $CONF:/etc/nginx/nginx.conf \
-v $HTML:/usr/share/nginx/html \
-v $LOGS:/var/log/nginx \
-v $SERV:/etc/nginx/conf.d \
--name $NGINX_NAME \
--restart=always \
nginx

























































































































































































































































































































































































































































































